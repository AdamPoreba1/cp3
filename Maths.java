public class Maths {
    static float larger(float a, float b) {
        return Math.max(a, b);
    }
    static float absolute(int a) {
        return Math.abs(a);
    }
    static double Square(int a, int b) {
        return Math.sqrt(a/b);
    } 
    static double sin(double a){
        return Math.sin(a);
    }
    
}
    
    
